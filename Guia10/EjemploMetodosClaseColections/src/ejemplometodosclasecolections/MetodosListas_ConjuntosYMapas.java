/*
LISTAS Y CONJUNTOS

Método Descripción.

size() Este método retorna el tamaño de una lista / conjunto.

clear() Este método se usa para remover todos los elementos de una lista / conjunto.

get(int índice) Este método retorna un elemento de la lista según un índice de la lista.

set(int índice, elemento) Este método guarda un elemento en la lista en un índice especifico.

isEmpty() Este método retorna verdadero si la lista / conjunto está vacío y falso si no lo está.

contains(elemento)Este método recibe un elemento dado por el usuario y revisa
                  si el elemento se encuentra en la lista o no. Si el elemento se
                  encuentra retorna verdadero y si no falso.



MAPAS



clear() Este método se usa para remover todos los elementos de un mapa.

containsKey(Llave) Este método recibe una llave dada por el usuario y revisa si la
                   llave se encuentra en la lista o no. Si la llave se encuentra
                   retorna verdadero y si no falso.

containsValue(Valor) Este método recibe un valor dado por el usuario y revisa si el
                     valor se encuentra en el mapa o no. Si el elemento se
                     encuentra retorna verdadero y si no falso.

get(Llave) Este método retorna un elemento del mapa según una llave dentro del mapa.

isEmpty() Este método retorna verdadero si el mapa está vacío y falso si no lo está.

size() Este método retorna el tamaño de un mapa.

values() Este método crea una colección según los valores del mapa.
         Ósea, que retorna una lista, por ejemplo, con todos los
         valores del mapa.

clear() Este método se usa para remover todos los elementos de un mapa.
*/